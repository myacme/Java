public class ChuAndYu {

	public static void main(String[] args) {
		System.out.println("3/2 = " + 3 / 2);
		System.out.println("3%2 = " + 3 % 2);
		System.out.println("-3/2 = " + -3 / 2);
		System.out.println("-3%2 = " + -3 % 2);
		System.out.println("3/-2 = " + 3 / -2);
		System.out.println("3%-2 = " + 3 % -2);
		System.out.println("-3/-2 = " + -3 / -2);
		System.out.println("-3%-2 = " + -3 % -2);
	}
}